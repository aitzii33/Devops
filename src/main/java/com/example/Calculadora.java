package com.example;

public class Calculadora {
    public int add(int a, int b) 
    {
        return a + b;
    }

    public int subtract(int a, int b) 
    {
        return a - b;
    }   
    
    public int multiply(int a, int b)
    {
        return a * b;
    }

    public float diveded (int a, int b)
    {
        return a / b;
    }

    public float media (int a, int b)
    {
        return (a + b) / 2.0f;
    }
}
